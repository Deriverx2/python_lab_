#Variable : A sequence of characters used to store the values
#Datatype : Type associated with a data

#int:(integer type)
digit=5
print(digit)
print(type(digit))

#float:(float or decimal type)
value=5.5
print(value)
print(type(value))

#string:(a literal character sequence)
name="John"
print(name)
print(type(name))

#boolean:(true or false)
isPrime=False
print(isPrime)
print(type(isPrime))

#complex

#complex:(You can put ‘j’ or ‘J’ after a number to make it imaginary, so you can write complex literals easily)
z = 3+4j
print(z)
print(type(z))